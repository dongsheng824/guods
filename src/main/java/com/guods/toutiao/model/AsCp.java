package com.guods.toutiao.model;

public class AsCp {

	private String as;
	private String cp;
	
	public AsCp(String as, String cp) {
		super();
		this.as = as;
		this.cp = cp;
	}
	public String getAs() {
		return as;
	}
	public void setAs(String as) {
		this.as = as;
	}
	public String getCp() {
		return cp;
	}
	public void setCp(String cp) {
		this.cp = cp;
	}
	
}
