package com.guods.toutiao.model;

public class LogExtra {

	private String rit;
	private Integer convert_id;
	private String req_id;
	private String ad_price;

	public String getRit() {
		return rit;
	}

	public void setRit(String rit) {
		this.rit = rit;
	}

	public Integer getConvert_id() {
		return convert_id;
	}

	public void setConvert_id(Integer convert_id) {
		this.convert_id = convert_id;
	}

	public String getReq_id() {
		return req_id;
	}

	public void setReq_id(String req_id) {
		this.req_id = req_id;
	}

	public String getAd_price() {
		return ad_price;
	}

	public void setAd_price(String ad_price) {
		this.ad_price = ad_price;
	}

}
